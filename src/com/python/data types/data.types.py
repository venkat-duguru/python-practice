#finding the type of a number
a = 5
print(type(a))


#finding the type of a sequence of letters
b = "Five"
print(type(b))


#finding the type of decimal values
c = 20.5
print(type(c))


#finding the type of a collection of elements
d = ["apple", "banana", "cherry"]
print(type(d))


#
e = ("apple", "banana", "cherry")
print(type(e))


#finding the type of a group of a key, value pair elements
f = {"name": "John", "age": 36}
print(type(f))


#finding the type of true or False elements
g = True
print(type(g))
