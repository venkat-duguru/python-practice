#write a program to print numbers from 1 to 10 by using while loop
x = 1
while x <= 10:
    print(x)
    x = x + 1


print("End of the program")


#write a program to print numbers from 10 to 1 using while loop
y = 10
while y >= 1:
    print(y)
    y = y - 1


print("End of the program because the value of y is:", y)


#write a program to print numbers from 1 to 10 by using for loop
for i in range(10):
    print(i)


#write a program to print the numbers from 10 to 1 using for loop
for j in range(10, 1):
    print(j)


